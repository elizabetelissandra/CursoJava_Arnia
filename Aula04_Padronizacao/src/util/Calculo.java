package util;

public class Calculo {

	public double somaValores(double valor1, double valor2) {
		return valor1 + valor2;
	}
	
	public double subtraiValores(double valor1, double valor2) {
		return valor1 - valor2;
	}
	
	public double divideValor(double divisor, double dividendo) {
		return  dividendo / divisor;
	}
	
	public double multiplicaValor(double valor1, double valor2) {
		return valor1 * valor2;
	}

}
